            師傅引進門，修行靠個人，師傅雖然重要，但拜師卻又不能完全依賴，老師能導以門徑，指點迷津，讓我們少走彎路。到底能走多遠還是在學習過程當中的堅持和思考。
          </p>
          <p class="second-part-title">第二個歷程：以古為師</p>
          <p class="second-part">
